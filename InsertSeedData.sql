IF OBJECT_ID('dbo.TABLESIZE') IS NOT NULL
  DROP FUNCTION dbo.TABLESIZE
GO

CREATE FUNCTION dbo.TABLESIZE(@tableName NVARCHAR(128))
RETURNS INT
AS
BEGIN
      DECLARE @count INT = (
            SELECT SUM([rows])
      FROM sys.partitions
      WHERE object_id=object_id(@tableName)
            AND index_id IN (0,1))
      RETURN @count
END
GO

-- equivalent de Math.max() en sql server
