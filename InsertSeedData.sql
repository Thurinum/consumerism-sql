-- offer.contract
DECLARE @i INT = dbo.TABLESIZE(N'Offer.Contract')
DECLARE @max INT = @i + CONVERT(INT, SESSION_CONTEXT(N'max'))
DECLARE @maxEntreprises INT = CONVERT(INT, SESSION_CONTEXT(N'max_entreprise'))
WHILE @i < @max
BEGIN
      INSERT INTO Offer.Contract
      VALUES
            (
                  'Description ' + LTRIM(@i),
                  RAND() * 100,
                  dbo.MAX(1, RAND() * @max),
                  dbo.MAX(1, RAND() * @maxEntreprises)
      )
      SET @i = @i + 1
END
PRINT 'Insertion des CONTRATS terminée.'
GO

