WHILE @i < @max
BEGIN
PRINT CAST(ROUND(RAND() * 1000000, 2) AS MONEY)
      DECLARE @isProduct BIT = (CASE WHEN RAND() < 0.5 THEN 1 ELSE 0 END)
      INSERT INTO Offer.Product
      VALUES (
                  (SELECT TOP 1 Name FROM @names ORDER BY NEWID()) + 
                  (CASE WHEN @isProduct = 1 THEN ' Service' ELSE '' END),
                  @isProduct,
                  RAND() * 1000000,
                  RAND() * 100
      )
      SET @i = @i + 1
END
PRINT 'Insertion des PRODUITS terminée.'
GO

-- offer.productinstance
DECLARE @i INT = dbo.TABLESIZE(N'Offer.ProductInstance')
DECLARE @max INT = @i + CONVERT(INT, SESSION_CONTEXT(N'max'))
WHILE @i < @max
BEGIN
      INSERT INTO Offer.ProductInstance
      VALUES
            (
                  dbo.MAX(1, RAND() * @max),
                  dbo.MAX(1, RAND() * @max)
      )
      SET @i = @i + 1
END
PRINT 'Insertion des INSTANCES DE PRODUITS terminée.'
GO

-- demand.transaction
DECLARE @i INT = dbo.TABLESIZE(N'Demand.Transaction')
DECLARE @max INT = @i + CONVERT(INT, SESSION_CONTEXT(N'max'))
DECLARE @productInstanceID INT
WHILE @i < @max
BEGIN
      SET @productInstanceID = dbo.MAX(1, RAND() * @max)
      INSERT INTO Demand.[Transaction]
      VALUES
            (
                  DATEADD(SECOND, RAND() * 10000, 
                  DATEADD(DAY, RAND() * 1000, '2000-01-01')),
                  (
				SELECT P.BaseValue
                        FROM Offer.Product as P
                        WHERE P.ProductID = @productInstanceID
			) + RAND() * 1000, -- taxes et frais de livraison
                  dbo.MAX(1, RAND() * @max),
                  dbo.MAX(1, RAND() * @max),
                  @productInstanceID
      )
      SET @i = @i + 1
END
PRINT 'Insertion des TRANSACTIONS terminée.'
GO

